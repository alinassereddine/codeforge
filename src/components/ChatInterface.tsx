/**
 * Main Chat Interface Component
 */
export function ChatInterface() {
    const [input, setInput] = useState('');
    const scrollRef = useRef<HTMLDivElement>(null);

    const {
        messages,
        isStreaming,
        currentStreamId,
        apiKey,
        isChatOpen,
        isSettingsOpen,
        addMessage,
        appendToMessage,
        startStreaming,
        stopStreaming,
        toggleChat,
        openSettings,
        closeSettings,
    } = useChatStore();

    const { container, setStatus, setFileTree } = useContainerStore();

    // Auto-scroll to bottom on new messages
    useEffect(() => {
        if (scrollRef.current) {
            scrollRef.current.scrollTop = scrollRef.current.scrollHeight;
        }
    }, [messages, currentStreamId]);

    /**
     * Handle sending a message
     */
    const handleSend = async () => {
        if (!input.trim() || isStreaming) return;

        const userMessage = input.trim();
        setInput('');

        // Add user message
        addMessage({ role: 'user', content: userMessage });

        // Start streaming response
        const streamId = startStreaming();

        // Create parser connected to WebContainer
        const parser = new StreamParser(container, {
            onFileWritten: async (filePath) => {
                console.log('[Chat] File written:', filePath);
                // Refresh file tree in UI after file is written
                if (container) {
                    try {
                        const newTree = await getFileTree(container);
                        setFileTree(newTree);
                        console.log('[Chat] File tree refreshed');
                    } catch (error) {
                        console.error('[Chat] Failed to refresh file tree:', error);
                    }
                }
            },
            onShellCommand: (command) => {
                console.log('[Chat] Shell command:', command);
                // Show installing badge
                if (command.includes('npm install')) {
                    setStatus('installing');
                }
            },
            onText: (chunk) => {
                // Append chunk to streaming message
                appendToMessage(streamId, chunk);
            },
        });

        try {
            if (apiKey) {
                // TODO: Implement real API call
                // For now, fall through to mock mode
                console.log('[Chat] API key present, but using mock mode for now');
            }

            // Mock mode: stream the mock response
            for await (const chunk of mockStream(MOCK_RESPONSE)) {
                await parser.processChunk(chunk);
            }

            parser.finalize();
        } catch (error) {
            console.error('[Chat] Error:', error);
            appendToMessage(streamId, '\n\nâŒ An error occurred while generating the response.');
        } finally {
            stopStreaming();
        }
    };

    /**
     * Handle Enter key press
     */
    const handleKeyDown = (e: React.KeyboardEvent) => {
        if (e.key === 'Enter' && !e.shiftKey) {
            e.preventDefault();
            handleSend();
        }
    };

    return (
        <>
            {/* Chat Toggle Button */}
            <Sheet open={isChatOpen} onOpenChange={toggleChat}>
                <SheetTrigger asChild>
                    <Button
                        size="icon"
                        className={cn(
                            "fixed bottom-6 right-6 z-50 h-14 w-14 rounded-full shadow-lg",
                            "bg-gradient-to-br from-violet-600 to-cyan-600 hover:from-violet-500 hover:to-cyan-500",
                            "transition-all duration-300",
                            isChatOpen && "scale-0 opacity-0"
                        )}
                    >
                        <MessageSquare className="h-6 w-6" />
                    </Button>
                </SheetTrigger>

                <SheetContent
                    side="right"
                    className="w-full sm:w-[450px] p-0 flex flex-col bg-background/95 backdrop-blur-xl"
                >
                    {/* Header */}
                    <SheetHeader className="p-4 border-b border-border flex-shrink-0">
                        <div className="flex items-center justify-between">
                            <SheetTitle className="flex items-center gap-2 text-lg">
                                <div className="w-8 h-8 rounded-lg bg-gradient-to-br from-violet-500/20 to-cyan-500/20 flex items-center justify-center">
                                    <Sparkles className="w-4 h-4 text-cyan-400" />
                                </div>
                                <span className="gradient-text">AI Assistant</span>
                            </SheetTitle>
                            <div className="flex items-center gap-1">
                                <Button
                                    variant="ghost"
                                    size="icon"
                                    onClick={() => openSettings()}
                                    className="text-muted-foreground hover:text-foreground"
                                >
                                    <Settings className="w-5 h-5" />
                                </Button>
                                <Button
                                    variant="ghost"
                                    size="icon"
                                    onClick={() => toggleChat()}
                                    className="text-muted-foreground hover:text-foreground"
                                >
                                    <X className="w-5 h-5" />
                                </Button>
                            </div>
                        </div>

                        {/* Mode indicator */}
                        {!apiKey && (
                            <div className="mt-2 px-3 py-1.5 bg-amber-500/10 border border-amber-500/20 rounded-lg">
                                <p className="text-xs text-amber-400 flex items-center gap-2">
                                    <Sparkles className="w-3 h-3" />
                                    Mock Mode â€” Click settings to add API key
                                </p>
                            </div>
                        )}
                    </SheetHeader>

                    {/* Messages */}
                    <ScrollArea
                        ref={scrollRef}
                        className="flex-1 overflow-y-auto"
                    >
                        <div className="py-4">
                            {messages.length === 0 ? (
                                <div className="flex flex-col items-center justify-center h-64 text-center px-4">
                                    <div className="w-16 h-16 rounded-2xl bg-gradient-to-br from-violet-500/20 to-cyan-500/20 flex items-center justify-center mb-4">
                                        <Bot className="w-8 h-8 text-cyan-400" />
                                    </div>
                                    <h3 className="text-lg font-medium mb-2">Start a conversation</h3>
                                    <p className="text-sm text-muted-foreground max-w-xs">
                                        Describe what you want to build and I'll generate the code for you in real-time.
                                    </p>
                                </div>
                            ) : (
                                messages.map((msg) => (
                                    <MessageBubble
                                        key={msg.id}
                                        role={msg.role}
                                        content={msg.content}
                                        isStreaming={msg.isStreaming}
                                    />
                                ))
                            )}
                        </div>
                    </ScrollArea>

                    {/* Input */}
                    <div className="p-4 border-t border-border flex-shrink-0">
                        <div className="flex gap-2">
                            <Input
                                value={input}
                                onChange={(e) => setInput(e.target.value)}
                                onKeyDown={handleKeyDown}
                                placeholder="Describe what you want to build..."
                                disabled={isStreaming}
                                className="flex-1"
                            />
                            <Button
                                onClick={handleSend}
                                disabled={!input.trim() || isStreaming}
                                className="bg-gradient-to-r from-violet-600 to-cyan-600 hover:from-violet-500 hover:to-cyan-500"
                            >
                                {isStreaming ? (
                                    <Loader2 className="w-4 h-4 animate-spin" />
                                ) : (
                                    <Send className="w-4 h-4" />
                                )}
                            </Button>
                        </div>
                    </div>
                </SheetContent>
            </Sheet>

            {/* Settings Dialog */}
            <SettingsDialog
                open={isSettingsOpen}
                onOpenChange={(open) => open ? openSettings() : closeSettings()}
            />
        </>
    );
}
