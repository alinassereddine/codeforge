/**
 * Main Chat Panel
 */
export function ChatPanel() {
    const [input, setInput] = useState('');
    const [isSettingsOpen, setIsSettingsOpen] = useState(false);
    const [currentArtifact, setCurrentArtifact] = useState<ArtifactState | null>(null);
    const scrollRef = useRef<HTMLDivElement>(null);

    const {
        messages,
        isStreaming,
        currentStreamId,
        apiKey,
        apiProvider,
        selectedModel,
        chatMode,
        addMessage,
        appendToMessage,
        startStreaming,
        stopStreaming,
        setChatMode,
    } = useChatStore();

    const { container, setStatus, setFileTree } = useContainerStore();

    useEffect(() => {
        if (scrollRef.current) {
            scrollRef.current.scrollTop = scrollRef.current.scrollHeight;
        }
    }, [messages, currentStreamId, currentArtifact]);

    const handleSend = async () => {
        if (!input.trim() || isStreaming) return;

        const userMessage = input.trim();
        setInput('');
        addMessage({ role: 'user', content: userMessage });
        const streamId = startStreaming();

        // Reset artifact state
        setCurrentArtifact(null);

        const parser = new StreamParser(container, {
            onArtifactStart: (id, title) => {
                setCurrentArtifact({
                    isGenerating: true,
                    isComplete: false,
                    title,
                    files: [],
                });
            },
            onArtifactEnd: () => {
                setCurrentArtifact(prev => prev ? { ...prev, isGenerating: false, isComplete: true } : null);
            },
            onActionStart: (type, filePath) => {
                if (type === 'file' && filePath) {
                    setCurrentArtifact(prev => prev ? {
                        ...prev,
                        files: [...prev.files, { path: filePath, status: 'writing' }],
                    } : null);
                }
            },
            onFileWritten: async (filePath) => {
                console.log('[Chat] File written:', filePath);
                // Update file status to complete
                setCurrentArtifact(prev => prev ? {
                    ...prev,
                    files: prev.files.map(f => f.path === filePath ? { ...f, status: 'complete' as const } : f),
                } : null);

                // Refresh file tree
                if (container) {
                    try {
                        const newTree = await getFileTree(container);
                        setFileTree(newTree);
                        console.log('[Chat] File tree refreshed');
                    } catch (error) {
                        console.error('[Chat] Failed to refresh file tree:', error);
                    }
                }
            },
            onShellCommand: (command) => {
                if (command.includes('npm install')) setStatus('installing');
            },
            onText: (chunk) => appendToMessage(streamId, chunk),
        });

        try {
            // Check if we should use real API or mock
            const useRealApi = apiKey && apiProvider === 'openrouter';

            if (useRealApi) {
                // Use real OpenRouter API
                console.log('[Chat] Using OpenRouter API');

                // Get conversation history for context
                const conversationHistory = messages.map(m => ({
                    role: m.role,
                    content: m.content,
                }));

                // Add current user message
                conversationHistory.push({ role: 'user', content: userMessage });

                // Select system prompt based on mode
                const systemPrompt = chatMode === 'agent' ? SYSTEM_PROMPT : CHAT_SYSTEM_PROMPT;

                // If in agent mode, we use the specific system prompt in the API call
                // But streamOpenRouter adds SYSTEM_PROMPT by default, so we need to override it or modify streamOpenRouter
                // Let's modify the messages array directly to include the correct system prompt

                const apiMessages = [
                    { role: 'system', content: systemPrompt },
                    ...conversationHistory
                ];

                await streamOpenRouter({
                    apiKey,
                    model: selectedModel as import('@/lib/ai/openrouter').OpenRouterModel,
                    messages: conversationHistory, // We'll pass history, but we need to tell streamOpenRouter which prompt to use
                    systemPromptOverride: systemPrompt,
                    onChunk: async (chunk) => {
                        await parser.processChunk(chunk);
                    },
                    onError: (error) => {
                        console.error('[Chat] OpenRouter error:', error);
                        appendToMessage(streamId, `\n\nâŒ Error: ${error.message}`);
                    },
                    onComplete: () => {
                        parser.finalize();
                        console.log('[Chat] Stream complete');
                    },
                });
            } else {
                // Use mock response for demo
                console.log('[Chat] Using mock response (no API key or provider not openrouter)');
                for await (const chunk of mockStream(MOCK_RESPONSE)) {
                    await parser.processChunk(chunk);
                }
                parser.finalize();
            }
        } catch (error) {
            console.error('[Chat] Error:', error);
            appendToMessage(streamId, '\n\nâŒ An error occurred.');
        } finally {
            stopStreaming();
        }
    };

    // Get artifact for the current/last assistant message
    const getArtifactForMessage = (msgId: string) => {
        if (currentStreamId === msgId) {
            return currentArtifact;
        }
        return null;
    };

    return (
        <div className="flex flex-col h-full bg-background">
            {/* Header */}
            <div className="flex items-center justify-between px-3 py-2 border-b border-border bg-card">
                <div className="flex items-center gap-2">
                    <Sparkles className="w-4 h-4 text-cyan-400" />
                    <span className="text-sm font-medium">AI Assistant</span>
                </div>

                {/* Mode Toggle */}
                <div className="flex bg-muted rounded-md p-0.5 mx-2">
                    <button
                        onClick={() => setChatMode('agent')}
                        className={cn(
                            "px-2 py-0.5 text-xs font-medium rounded-sm transition-colors",
                            chatMode === 'agent' ? "bg-background text-foreground shadow-sm" : "text-muted-foreground hover:text-foreground"
                        )}
                    >
                        Agent
                    </button>
                    <button
                        onClick={() => setChatMode('chat')}
                        className={cn(
                            "px-2 py-0.5 text-xs font-medium rounded-sm transition-colors",
                            chatMode === 'chat' ? "bg-background text-foreground shadow-sm" : "text-muted-foreground hover:text-foreground"
                        )}
                    >
                        Chat
                    </button>
                </div>

                <div className="flex-1" />

                <Button variant="ghost" size="icon" className="h-6 w-6" onClick={() => setIsSettingsOpen(true)}>
                    <Settings className="w-3.5 h-3.5" />
                </Button>
            </div>

            {/* Mock Mode Banner */}
            {!apiKey && (
                <div className="px-3 py-1.5 bg-amber-500/10 border-b border-amber-500/20">
                    <p className="text-xs text-amber-400 flex items-center gap-1">
                        <Sparkles className="w-3 h-3" /> Mock Mode
                    </p>
                </div>
            )}

            {/* Messages */}
            <ScrollArea ref={scrollRef} className="flex-1">
                <div className="py-2">
                    {messages.length === 0 ? (
                        <div className="flex flex-col items-center justify-center h-40 text-center px-4">
                            <Bot className="w-8 h-8 text-cyan-400 mb-2" />
                            <p className="text-xs text-muted-foreground">Describe what you want to build</p>
                        </div>
                    ) : (
                        messages.map((msg) => (
                            <MessageBubble
                                key={msg.id}
                                role={msg.role}
                                content={msg.content}
                                isStreaming={msg.isStreaming}
                                artifact={msg.role === 'assistant' ? getArtifactForMessage(msg.id) : null}
                            />
                        ))
                    )}
                </div>
            </ScrollArea>

            {/* Input */}
            <div className="p-2 border-t border-border">
                <div className="flex gap-1.5">
                    <Input
                        value={input}
                        onChange={(e) => setInput(e.target.value)}
                        onKeyDown={(e) => e.key === 'Enter' && !e.shiftKey && (e.preventDefault(), handleSend())}
                        placeholder="Ask AI..."
                        disabled={isStreaming}
                        className="flex-1 h-8 text-xs"
                    />
                    <Button
                        onClick={handleSend}
                        disabled={!input.trim() || isStreaming}
                        size="icon"
                        className="h-8 w-8 bg-gradient-to-r from-violet-600 to-cyan-600"
                    >
                        {isStreaming ? <Loader2 className="w-3.5 h-3.5 animate-spin" /> : <Send className="w-3.5 h-3.5" />}
                    </Button>
                </div>
            </div>

            <SettingsDialog open={isSettingsOpen} onOpenChange={setIsSettingsOpen} />
        </div>
    );
}
