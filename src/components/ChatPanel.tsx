async function* mockStream(text: string): AsyncGenerator<string, void, unknown> {
    const words = text.split(' ');
    for (const word of words) {
        yield word + ' ';
        await new Promise(resolve => setTimeout(resolve, 15 + Math.random() * 25));
    }
}

// Track file actions for display
interface FileAction {
    path: string;
    status: 'writing' | 'complete';
}

interface ArtifactState {
    isGenerating: boolean;
    isComplete: boolean;
    title: string;
    files: FileAction[];
}

/**
 * Collapsible artifact card showing generation progress
 */
function ArtifactCard({
    artifact,
    isExpanded,
    onToggle
}: {
    artifact: ArtifactState;
    isExpanded: boolean;
    onToggle: () => void;
}) {
    return (
        <div className="mx-2 my-2 rounded-lg border border-border bg-card overflow-hidden">
            <Collapsible open={isExpanded} onOpenChange={onToggle}>
                <CollapsibleTrigger asChild>
                    <button className="w-full flex items-center gap-2 p-2 hover:bg-muted/50 transition-colors">
                        {artifact.isGenerating ? (
                            <Loader2 className="w-4 h-4 text-cyan-400 animate-spin flex-shrink-0" />
                        ) : (
                            <Check className="w-4 h-4 text-green-400 flex-shrink-0" />
                        )}
                        <span className="text-xs font-medium flex-1 text-left truncate">
                            {artifact.isGenerating ? 'Generating code...' : artifact.title || 'Code generated'}
                        </span>
                        <span className="text-xs text-muted-foreground">
                            {artifact.files.length} file{artifact.files.length !== 1 ? 's' : ''}
                        </span>
                        {isExpanded ? (
                            <ChevronDown className="w-3.5 h-3.5 text-muted-foreground" />
                        ) : (
                            <ChevronRight className="w-3.5 h-3.5 text-muted-foreground" />
                        )}
                    </button>
                </CollapsibleTrigger>
                <CollapsibleContent>
                    <div className="border-t border-border bg-muted/30 p-2 space-y-1">
                        {artifact.files.map((file, idx) => (
                            <div key={idx} className="flex items-center gap-2 text-xs py-1 px-2 rounded bg-background/50">
                                {file.status === 'writing' ? (
                                    <Loader2 className="w-3 h-3 text-amber-400 animate-spin flex-shrink-0" />
                                ) : (
                                    <FileCode className="w-3 h-3 text-green-400 flex-shrink-0" />
                                )}
                                <span className="font-mono truncate">{file.path}</span>
                            </div>
                        ))}
                    </div>
                </CollapsibleContent>
            </Collapsible>
        </div>
    );
}

/**
 * Message bubble with smart content display
 */
function MessageBubble({
    role,
    content,
    isStreaming,
    artifact
}: {
    role: 'user' | 'assistant';
    content: string;
    isStreaming?: boolean;
    artifact?: ArtifactState | null;
}) {
    const [isExpanded, setIsExpanded] = useState(false);
    const isUser = role === 'user';

    // Extract only the text content (before/after XML tags)
    const getTextContent = (text: string) => {
        // Remove all XML content
        return text
            .replace(/<boltArtifact[\s\S]*?<\/boltArtifact>/g, '')
            .replace(/<boltArtifact[^>]*>/g, '')
            .replace(/<\/boltArtifact>/g, '')
            .replace(/<boltAction[\s\S]*?<\/boltAction>/g, '')
            .replace(/<boltAction[^>]*>/g, '')
            .replace(/<\/boltAction>/g, '')
            .trim();
    };

    const textContent = getTextContent(content);
    const hasArtifact = artifact && artifact.files.length > 0;

    if (isUser) {
        return (
            <div className="flex gap-2 p-3 flex-row-reverse">
                <div className="flex-shrink-0 w-6 h-6 rounded-full flex items-center justify-center bg-primary/20 text-primary">
                    <User className="w-3 h-3" />
                </div>
                <div className="flex-1 max-w-[85%] text-right">
                    <div className="inline-block p-2 rounded-xl text-xs leading-relaxed bg-primary text-primary-foreground rounded-br-sm">
                        {content}
                    </div>
                </div>
            </div>
        );
    }

    return (
        <div className="flex gap-2 p-3 flex-row">
            <div className="flex-shrink-0 w-6 h-6 rounded-full flex items-center justify-center bg-violet-500/20 text-cyan-400">
                <Bot className="w-3 h-3" />
            </div>
            <div className="flex-1 max-w-[90%] space-y-2">
                {/* Text content */}
                {textContent && (
                    <div className="p-2 rounded-xl text-xs leading-relaxed bg-muted/50 text-foreground rounded-bl-sm">
                        <div className="whitespace-pre-wrap break-words">
                            {textContent}
                            {isStreaming && !hasArtifact && (
                                <span className="inline-block w-1.5 h-3 ml-0.5 bg-current animate-pulse" />
                            )}
                        </div>
                    </div>
                )}

                {/* Artifact card */}
                {hasArtifact && (
                    <ArtifactCard
                        artifact={artifact}
                        isExpanded={isExpanded}
                        onToggle={() => setIsExpanded(!isExpanded)}
                    />
                )}
            </div>
        </div>
    );
}

